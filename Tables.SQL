allstarID INT,
coast VARCHAR2(15),
 CONSTRAINT PK_ALLSTARCOAST PRIMARY KEY (ALLSTARID, COAST),
  CONSTRAINT FK_ALLSTARID FOREIGN KEY (ALLSTARID) REFERENCES XPLAYER (PLAYERID) 
);



